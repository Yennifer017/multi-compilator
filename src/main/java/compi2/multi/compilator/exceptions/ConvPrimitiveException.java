
package compi2.multi.compilator.exceptions;

/**
 *
 * @author blue-dragon
 */
public class ConvPrimitiveException extends Exception {

    /**
     * Creates a new instance of <code>ConvPrimitiveException</code> without
     * detail message.
     */
    public ConvPrimitiveException() {
    }

    /**
     * Constructs an instance of <code>ConvPrimitiveException</code> with the
     * specified detail message.
     *
     * @param msg the detail message.
     */
    public ConvPrimitiveException(String msg) {
        super(msg);
    }
}
